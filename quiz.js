function show(count) {
    let questionContainer = document.getElementById("questions");
    let progressContainer = document.getElementById("progress");
    let progressBar = document.getElementById("progress-bar");

    questionContainer.innerHTML = "";
    let currentQuestion = questions[count];
    let questionElement = document.createElement("div");
    questionElement.classList.add("question");
    let questionText = document.createElement("p");
    questionText.textContent = currentQuestion.question;
    questionElement.appendChild(questionText);

    if (currentQuestion.image) {
        let imageElement = document.createElement("img");
        imageElement.src = currentQuestion.image;
        questionElement.appendChild(imageElement);
    }

    if (currentQuestion.domain === "CreativeExpression") {
        let answerInput = document.createElement("input");
        answerInput.type = "text";
        answerInput.classList.add("creative-answer");
        answerInput.placeholder = "Type your answer here...";
        
        // Convert input to lowercase as the user types
        answerInput.addEventListener('input', function() {
          this.value = this.value.toLowerCase();
      });

        questionElement.appendChild(answerInput);
        let nextButton = document.createElement("button");
        nextButton.textContent = "Next";
        nextButton.classList.add("next-btn");
        nextButton.onclick = () => {
            processSelection(answerInput, currentQuestion);
        };
        questionElement.appendChild(nextButton);
    } else {
        let optionsList = document.createElement("ul");
        optionsList.classList.add("option_group");
        currentQuestion.options.forEach(option => {
            let optionItem = document.createElement("li");
            optionItem.classList.add("option");
            optionItem.textContent = option.value || option;
            optionItem.onclick = () => processSelection(optionItem, currentQuestion);
            optionsList.appendChild(optionItem);
        });
        questionElement.appendChild(optionsList);
    }

    questionContainer.appendChild(questionElement);
     // Update progress indicator
     progressContainer.textContent = `Question ${count + 1} of ${questions.length}`;
     progressBar.style.width = `${((count + 1) / questions.length) * 100}%`;
    restartTimer();  // Restart timer for each new question
}

function restartTimer() {
    clearInterval(timer);
    let timeInSeconds = 30;
    let timerDisplay = document.getElementById('timer');
    timerDisplay.textContent = timeInSeconds;
    timer = setInterval(() => {
        timerDisplay.textContent = --timeInSeconds;
        if (timeInSeconds <= 0) {
            clearInterval(timer);
            nextQuestion();
        }
    }, 1000);
}

function processSelection(selectedItem, currentQuestion) {
  let selectedValue = selectedItem.value || selectedItem.textContent.trim();

   // Convert input to lowercase if the question is from the Creative Expression domain
   if (currentQuestion.domain === "CreativeExpression") {
    selectedValue = selectedValue.toLowerCase();
}
  let domain = currentQuestion.domain;
  let subdomain = currentQuestion.subdomain;

  // Check if the current question has scored options
  if (currentQuestion.options && currentQuestion.options[0].hasOwnProperty('score')) {
      let score = currentQuestion.options.find(opt => opt.value === selectedValue).score;
      if (subdomain && domainScores[domain][subdomain] !== undefined) {
          domainScores[domain][subdomain] += score;
      } else {
          domainScores[domain] += score;
      }
  } else {
      // Non-scored questions (e.g., Vocabulary)
      if (selectedValue === currentQuestion.answer) {
          domainScores[domain]++;
      }
  }

  // Automatically move to the next question
  nextQuestion();
}

function nextQuestion() {
    question_count++;
    if (question_count < questions.length) {
        show(question_count);
    } else {
        prepareSubmitButton();
    }
}

function calculatePercentageScores() {
    let percentageScores = {
        "Arithmetic": (domainScores["Arithmetic"] / totalQuestionsPerDomain["Arithmetic"]) * 100,
        "SpatialReasoning": (domainScores["SpatialReasoning"] / totalQuestionsPerDomain["SpatialReasoning"]) * 100,
        "Vocabulary": (domainScores["Vocabulary"] / totalQuestionsPerDomain["Vocabulary"]) * 100,
        "CreativeExpression": (domainScores["CreativeExpression"] / totalQuestionsPerDomain["CreativeExpression"]) * 100
    };
    return percentageScores;
}
function calculateSubdomainScores() {
  let scores = {
      Personality: {
          total: 0,
          subdomains: {}
      },
      EmotionalAwareness: {
          total: 0,
          subdomains: {}
      }
  };

  // Aggregate scores for Personality
  Object.keys(domainScores["Personality"]).forEach(sub => {
      scores.Personality.subdomains[sub] = domainScores["Personality"][sub];
      scores.Personality.total += domainScores["Personality"][sub];
  });

  // Aggregate scores for Emotional Awareness
  Object.keys(domainScores["EmotionalAwareness"]).forEach(sub => {
      scores.EmotionalAwareness.subdomains[sub] = domainScores["EmotionalAwareness"][sub];
      scores.EmotionalAwareness.total += domainScores["EmotionalAwareness"][sub];
  });

  return scores;
}

function displaySubdomainScores(subdomainScores) {
  let subdomainScoresContainer = document.getElementById("subdomain-scores");
  subdomainScoresContainer.innerHTML = "<h2>Subdomain Scores</h2>";

  for (let domain in subdomainScores) {
      let domainElement = document.createElement("div");
      domainElement.innerHTML = `<h3>${domain} - Total: ${subdomainScores[domain].total}</h3>`;
      for (let subdomain in subdomainScores[domain].subdomains) {
          let subdomainScoreElement = document.createElement("p");
          subdomainScoreElement.textContent = `${subdomain}: ${subdomainScores[domain].subdomains[subdomain]}`;
          domainElement.appendChild(subdomainScoreElement);
      }
      subdomainScoresContainer.appendChild(domainElement);
  }
}

function prepareSubmitButton() {
    let questionContainer = document.getElementById("questions");
    let submitButton = document.createElement("button");
    submitButton.textContent = "Submit";
    submitButton.classList.add("submit-btn");
    submitButton.onclick = finishQuiz;
    questionContainer.appendChild(submitButton);
}


function finishQuiz() {

  localStorage.setItem('domainScores', JSON.stringify(domainScores));
  localStorage.setItem('percentageScores', JSON.stringify(calculatePercentageScores()));
  localStorage.setItem('subdomainScores', JSON.stringify(calculateSubdomainScores()));

  window.location.href = 'final.html';
}


